// Arithmetic Calculation 
let a = 10, b = 3;

//Addition
let sum = a + b
console.log("This is the sum of a and b: ", sum)

//Subtraction
let diff = a - b
console.log("This is the difference of a and b: ", diff)

//multification
let product = a * b
console.log("This is the product of a and b: ", product)

//Division
let div = a / b
console.log("This is the division of a and b: ", div)

//Any number divided by 0
let result = a / 0;
console.log("The result is", result)

//Modulo / Remainder
let Remainder = 12 % 5
console.log("The remainder of 12 and 5 is", Remainder)

