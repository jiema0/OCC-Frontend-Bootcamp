// Looping statement
// i++  -> i = i+1
// ++ is an increment operator, wherein it adds 1 to the current value of a variable.

for (let i=1; i<=5; i++) {
    console.log("count: ", i)
};

//  while loop
let count = 1;
while (count <= 5) {
  console.log('Count:', count);
  count++;
}; 

// Loops: do-while loop
let i = 1;
do {
  console.log('Do-While Count:', i);
  i++;
} while (i <= 5);